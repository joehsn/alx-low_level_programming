#include "3-calc.h"

/**
 * op_add - an addition operation
 *
 * @a: 1st arg
 *
 * @b: 2nd arg
 *
 * Return: an intger of a + b
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - a subtruction operation
 *
 * @a: 1st arg
 *
 * @b: 2nd arg
 *
 * Return: an intger of a - b
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - a multiplication operation
 *
 * @a: 1st arg
 *
 * @b: 2nd arg
 *
 * Return: an intger of a - b
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - a division operation
 *
 * @a: 1st arg
 *
 * @b: 2nd arg
 *
 * Return: an intger of a / b
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - a modulus operation
 *
 * @a: 1st arg
 *
 * @b: 2nd arg
 *
 * Return: an intger of a % b
 */

int op_mod(int a, int b)
{
	return (a % b);
}
